/*
 * 03. Numbers form 1 to 20 
 * 
 * Type a C# console program that prints the numbers from 1 to 20 on separate rows on the console.
 */

using System;

namespace Numbers_from_1_to_20
{
    class MainClass
    {
        public static void Main()
        {
            for (int i = 1; i <= 20; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
