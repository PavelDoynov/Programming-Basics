/*
 * 03. Latin Letters
 * 
 * Write a program that prints all the letters of the Latin alphabet: a, b, c, ..., z.
*/

using System;

namespace Latin_letters
{
    class MainClass
    {
        public static void Main()
        {
            for (char i = 'a'; i <= 'z'; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
