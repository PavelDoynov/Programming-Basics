/*
 * 01. Numbers from 1 to 100
 * 
 * Write a program that prints the numbers from 1 to 100, one per line.
*/

using System;

namespace Numbers_from_1_to_100
{
    class MainClass
    {
        public static void Main()
        {
            for (int i = 1; i <= 100; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}